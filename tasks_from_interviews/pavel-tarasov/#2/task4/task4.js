function round5(val) {
    return Math.round(val/5) * 5;
}
console.log(round5(0));
console.log(round5(2));
console.log(round5(3));
console.log(round5(11));
console.log(round5(14));
console.log(round5(50));
console.log(round5(-2));
console.log(round5(-3)); // error